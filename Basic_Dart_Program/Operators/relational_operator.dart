// Relational operators are also called comparison operators.
// They are used to make a comparison.

void main() {
  int num1 = 10;
  int num2 = 5;

  print(num1 == num2); // output false;
  print(num1 < num2); // output false;
  print(num1 > num2); // output true;
  print(num1 <= num2); // output false;
  print(num1 >= num2); // output true;
  print(num1 != num2); // output true;
}
